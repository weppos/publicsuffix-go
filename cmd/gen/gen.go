package main

// +build ignore

import (
	"bytes"
	"fmt"
	"go/format"
	"io/ioutil"
	"net/http"
	"os"
	"regexp"
	"strings"
	"text/template"
	"time"

	"github.com/weppos/publicsuffix-go/publicsuffix"
)

const (
	goSrc = `// This file is automatically generated
// Run "go run cmd/gen/gen.go" to update the list.

package publicsuffix

const defaultListVersion = "PSL version {{.VersionSHA}} ({{.VersionDate}})"

func init() {
	r := [{{len .Rules}}]Rule{
		{{range $r := .Rules}} \
		{ {{$r.Type}}, "{{$r.Value}}", {{$r.Length}}, {{$r.Private}} },
		{{end}}
	}
	DefaultList.rules = r[:]
}

`
)

var (
	goTemplate = template.Must(template.New("").Parse(cont(goSrc)))
)

// https://github.com/golang/go/issues/9969
func cont(s string) string {
	return strings.Replace(s, "\\\n", "", -1)
}

func main() {
	sha, datetime := extractHeadInfo()

	resp, err := http.Get(fmt.Sprintf("https://raw.githubusercontent.com/publicsuffix/list/%s/public_suffix_list.dat", sha))
	if err != nil {
		fatal(err)
	}
	defer resp.Body.Close()

	list := publicsuffix.NewList()
	rules, err := list.Load(resp.Body, nil)
	if err != nil {
		fatal(err)
	}

	data := struct {
		VersionSHA  string
		VersionDate string
		Rules       []publicsuffix.Rule
	}{
		sha,
		datetime.Format(time.ANSIC),
		rules,
	}

	buf := new(bytes.Buffer)
	err = goTemplate.Execute(buf, &data)
	if err != nil {
		fatal(err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fatal(err)
	}
	_, err = os.Stdout.Write(formatted)
	//_, err = os.Stdout.Write(buf.Bytes())
}

// Is there a better way?
func extractHeadInfo() (sha string, datetime time.Time) {
	var re *regexp.Regexp
	resp, err := http.Get("https://github.com/publicsuffix/list")
	if err != nil {
		fatal(err)
	}
	defer resp.Body.Close()

	data, err := ioutil.ReadAll(resp.Body)
	if err != nil {
		fatal(err)
	}

	re = regexp.MustCompile(`<a class="commit-tease-sha" (?:.+)>([\w\s\n]+)<\/a>`)
	sha = strings.TrimSpace(re.FindStringSubmatch(string(data[:]))[1])
	if sha == "" {
		fatal(fmt.Errorf("sha is blank"))
	}

	re = regexp.MustCompile(`<span itemprop="dateModified">(?:.+)datetime="([\w\d\:\-]+)"(?:.+)</span>`)
	stringtime := re.FindStringSubmatch(string(data[:]))[1]
	if stringtime == "" {
		fatal(fmt.Errorf("date is blank"))
	}
	datetime, err = time.Parse(time.RFC3339, stringtime)
	if err != nil {
		fatal(err)
	}

	return sha, datetime
}

func fatal(err error) {
	fmt.Println(err)
	os.Exit(1)
}
